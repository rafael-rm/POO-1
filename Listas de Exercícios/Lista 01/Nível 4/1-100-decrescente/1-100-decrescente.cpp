// 1-100-decrescente.cpp : Este arquivo contém a função 'main'. A execução do programa começa e termina ali.
//

#include <iostream>

using namespace std;

int main()
{
    cout << "\n\tImprimir de 1 a 100 em ordem decrescente\n\n";
	
	for (int i = 100; i >= 1; i--)
	{
		cout << i << " ";
	}
	
	cout << endl << endl;
	
	return 0;
}


